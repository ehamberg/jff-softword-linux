void gradiant(int i,int j,int k,float *N1)
{
  static int off;

  if(i+1 >=N) i=N-2;
  if(i-1 <0 ) i=1;
  if(j+1 >=N) j=N-1;
  if(j-1 <0 ) j=1;
  if(k+1 >=N) k=N-2;
  if(k-1 <0 ) k=1;

  off=i+(j<<N2);

  N1[0]=-F[k  ][off+1] + F[k  ][off-1];
  N1[1]=-F[k  ][off+N] + F[k  ][off-N];
  N1[2]=-F[k+1][off  ] + F[k-1][off  ];
}



